/**
 * Simple keyword-based search for RAG retrieval.
 * In production, this would use vector similarity search with embeddings.
 */
export function searchTipitaka(query: string, topK: number = 3): TipitakaEntry[] {
  const queryLower = query.toLowerCase();
  const queryWords = queryLower.split(/[\s,]+/).filter(w => w.length > 1);

  const scored = TIPITAKA_CORPUS.map(entry => {
    let score = 0;

    for (const word of queryWords) {
      // Check keywords (highest weight)
      for (const kw of entry.keywords) {
        if (kw.includes(word) || word.includes(kw)) {
          score += 10;
        }
      }
      // Check content
      if (entry.content.includes(word)) score += 3;
      // Check title
      if (entry.thaiTitle.includes(word)) score += 5;
      if (entry.practicalApplication.includes(word)) score += 2;
    }

    return { entry, score };
  });

  return scored
    .filter(s => s.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, topK)
    .map(s => s.entry);
}
